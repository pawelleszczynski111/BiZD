  CREATE OR REPLACE EDITIONABLE PROCEDURE "LESZCZYNSKIP"."GENERUJ_ZESTAWIENIA" (
    typ IN VARCHAR
) AS
BEGIN
    IF typ = 'MIESIECZNE' THEN
        INSERT INTO Zestawienia (typ_zestawienia, okres, suma_sprzedanych, suma_przychodow)
        SELECT 'MIESIECZNE', 
               TO_CHAR(z.data_zamowienia, 'YYYY-MM') AS okres,
               SUM(pz.ilosc) AS suma_sprzedanych,
               SUM(pz.ilosc * p.cena) AS suma_przychodow
        FROM Zamowienia z
        JOIN ProduktyZamowienia pz ON z.id_zamowienia = pz.id_zamowienia
        JOIN Produkty p ON pz.id_produktu = p.id_produktu
        GROUP BY TO_CHAR(z.data_zamowienia, 'YYYY-MM');

    ELSIF typ = 'KWARTALNE' THEN
        INSERT INTO Zestawienia (typ_zestawienia, okres, suma_sprzedanych, suma_przychodow)
        SELECT 'KWARTALNE', 
               TO_CHAR(z.data_zamowienia, 'YYYY-"Q"Q') AS okres,
               SUM(pz.ilosc) AS suma_sprzedanych,
               SUM(pz.ilosc * p.cena) AS suma_przychodow
        FROM Zamowienia z
        JOIN ProduktyZamowienia pz ON z.id_zamowienia = pz.id_zamowienia
        JOIN Produkty p ON pz.id_produktu = p.id_produktu
        GROUP BY TO_CHAR(z.data_zamowienia, 'YYYY-"Q"Q');

    ELSIF typ = 'ROCZNE' THEN
        INSERT INTO Zestawienia (typ_zestawienia, okres, suma_sprzedanych, suma_przychodow)
        SELECT 'ROCZNE', 
               TO_CHAR(z.data_zamowienia, 'YYYY') AS okres,
               SUM(pz.ilosc) AS suma_sprzedanych,
               SUM(pz.ilosc * p.cena) AS suma_przychodow
        FROM Zamowienia z
        JOIN ProduktyZamowienia pz ON z.id_zamowienia = pz.id_zamowienia
        JOIN Produkty p ON pz.id_produktu = p.id_produktu
        GROUP BY TO_CHAR(z.data_zamowienia, 'YYYY');
    END IF;
END;

